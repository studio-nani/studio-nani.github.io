const ORDERED_PRODUCTS = ["fortune", "myFlow"];
const ALLOWED_LANGS = ["ko", "en", "ja"];
const SOURCES = ["tiktok", "youtube", "instagram", "other"];
const params = new URLSearchParams(window.location.search);
const source = (params.get("src") || params.get("utm_source") || "other").toLowerCase();
const safeSource = SOURCES.includes(source) ? source : "other";

function resolveLanguage() {
  const queryLang = (params.get("lang") || "").toLowerCase();
  if (ALLOWED_LANGS.includes(queryLang)) return queryLang;

  const storedLang = (window.localStorage.getItem("studio-nani-lang") || "").toLowerCase();
  if (ALLOWED_LANGS.includes(storedLang)) return storedLang;

  const browser = (navigator.language || "ko").toLowerCase();
  if (browser.startsWith("en")) return "en";
  if (browser.startsWith("ja")) return "ja";
  return "ko";
}

let currentLang = resolveLanguage();
let t = I18N[currentLang];

function getText(path) {
  return path.split(".").reduce((value, key) => value?.[key], t) || "";
}

function productSlug(productKey) {
  return productKey === "myFlow" ? "my_flow" : productKey;
}

function withTracking(url, productKey, store) {
  try {
    const target = new URL(url, window.location.origin);
    target.searchParams.set("utm_source", safeSource);
    target.searchParams.set("utm_medium", "profile_link");
    target.searchParams.set("utm_campaign", "studio_nani");
    target.searchParams.set("utm_content", `${productSlug(productKey)}_${store}`);
    return target.toString();
  } catch (_error) {
    return url;
  }
}

function withPageParams(url) {
  const target = new URL(url, window.location.origin);
  if (safeSource !== "other") target.searchParams.set("src", safeSource);
  target.searchParams.set("lang", currentLang);
  return `${target.pathname}${target.search}${target.hash}`;
}

function renderStaticText() {
  document.documentElement.lang = currentLang;
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const value = getText(el.dataset.i18n);
    if (value) el.textContent = value;
  });

  document.querySelectorAll(".lang-btn").forEach((button) => {
    const isActive = button.dataset.lang === currentLang;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });
}

function renderStoreButtons(productKey, includeProductPage) {
  const product = PRODUCTS[productKey];
  const storeButtons = product.stores
    .map((store, index) => {
      const className = index === 0 ? "btn" : "btn secondary";
      return `<a class="${className}" href="${withTracking(store.url, productKey, store.type)}">${t.buttons[store.labelKey]}</a>`;
    })
    .join("");

  const pageButton = includeProductPage
    ? `<a class="btn secondary" href="${withPageParams(product.pageUrl)}">${t.buttons.productPage}</a>`
    : "";

  return `${storeButtons}${pageButton}`;
}

function renderApps() {
  const root = document.getElementById("app-list");
  if (!root) return;

  root.innerHTML = ORDERED_PRODUCTS.map((productKey) => {
    const product = PRODUCTS[productKey];
    const copy = t.products[productKey];
    return `
      <article class="card">
        <div class="app-main">
          <img src="${product.iconUrl}" alt="${copy.name} icon" class="app-icon" />
          <div class="app-copy">
            <h3>${copy.name}</h3>
            <p>${copy.description}</p>
          </div>
        </div>
        <div class="btn-group">
          ${renderStoreButtons(productKey, true)}
        </div>
      </article>
    `;
  }).join("");
}

function renderSocialLinks() {
  const root = document.getElementById("social-links");
  if (!root) return;

  root.innerHTML = t.links
    .map(
      (link) => `
      <a class="link-row" href="${link.url}" target="_blank" rel="noreferrer">
        <div class="link-main">
          <span class="link-badge" aria-hidden="true">${link.badge}</span>
          <div class="link-copy">
            <p class="link-title">${link.name}</p>
            <p class="link-description">${link.description}</p>
          </div>
        </div>
        <span class="link-arrow" aria-hidden="true">↗</span>
      </a>
    `
    )
    .join("");
}

function renderProductPage() {
  const productKey = document.body.dataset.product;
  const product = PRODUCTS[productKey];
  const copy = t.products[productKey];
  if (!product || !copy) return;

  document.title = `${copy.name} | Studio Nani`;
  document.getElementById("product-icon").src = product.iconUrl;
  document.getElementById("product-icon").alt = `${copy.name} icon`;
  document.getElementById("product-kicker").textContent = copy.kicker;
  document.getElementById("product-title").textContent = copy.name;
  document.getElementById("product-description").textContent = copy.detailDescription;

  document.getElementById("product-actions").innerHTML = renderStoreButtons(productKey, false);
  document.getElementById("product-features").innerHTML = copy.features.map((item) => `<li>${item}</li>`).join("");

  const legalRoot = document.getElementById("legal-links");
  if (!legalRoot || !product.legalLinks) return;

  legalRoot.innerHTML = product.legalLinks
    .map(
      (link) => `
      <a class="link-row" href="${link.url}">
        <div class="link-main">
          <span class="link-badge" aria-hidden="true">DOC</span>
          <div class="link-copy">
            <p class="link-title">${t.legal[link.key]}</p>
          </div>
        </div>
        <span class="link-arrow" aria-hidden="true">→</span>
      </a>
    `
    )
    .join("");
}

function propagateSourceLinks() {
  document.querySelectorAll("a[data-propagate-source]").forEach((link) => {
    link.href = withPageParams(link.getAttribute("href"));
  });
}

function updateLanguageState(nextLang) {
  currentLang = nextLang;
  t = I18N[currentLang];
  window.localStorage.setItem("studio-nani-lang", currentLang);

  const nextParams = new URLSearchParams(window.location.search);
  nextParams.set("lang", currentLang);
  if (safeSource !== "other") nextParams.set("src", safeSource);
  window.history.replaceState({}, "", `${window.location.pathname}?${nextParams.toString()}`);
}

function render() {
  renderStaticText();
  renderApps();
  renderSocialLinks();
  renderProductPage();
  propagateSourceLinks();
}

function bindLanguageSwitch() {
  document.querySelectorAll(".lang-btn").forEach((button) => {
    button.addEventListener("click", () => {
      const nextLang = button.dataset.lang;
      if (!ALLOWED_LANGS.includes(nextLang) || nextLang === currentLang) return;
      updateLanguageState(nextLang);
      render();
    });
  });
}

bindLanguageSwitch();
render();
